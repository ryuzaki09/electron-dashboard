const baseURL = 'https://demo.photoprism.app/api/v1'

function transformPhotoToViewDto(data) {
  return data.map((d) => ({
    id: d.ID,
    uid: d.UID,
    takenAt: d.TakenAt,
    takenAtLocal: d.TakenAtLocal,
    timeZone: d.TimeZone,
    name: d.Name,
    title: d.Title,
    caption: d.Caption,
    year: d.Year,
    month: d.Month,
    d: d.Day,
    placeCity: d.PlaceCity,
    url: `${baseURL}/t/${d.Hash}/public/${sizeOptions.fit1280}`
  }))
}
